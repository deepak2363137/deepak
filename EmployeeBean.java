package pack26;

public class EmployeeBean {

	private int empno;
	private String name;
	private int salary;
	private EmpAddr empdr;
	public int getEmpno() {
		return empno;
	}
	public void setEmpno(int empno) {
		this.empno = empno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public EmpAddr getEmpdr() {
		return empdr;
	}
	public void setEmpdr(EmpAddr empdr) {
		this.empdr = empdr;
	}
	
}
